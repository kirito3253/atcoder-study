# D: 1年の日数, F: 今年の最初のコンテストの日
d, f = map(int, input().split())

# 今年の最初のコンテスト(F日)から、今年の最後の日(D日)までの日数は D - F 日。
# コンテストは7日おきなので、最後のコンテストから年末までの「余り」の日数を計算します。
rem = (d - f) % 7

# 最後のコンテストの「7日後」が、翌年の最初のコンテストになります。
# 翌年の1日目を基準にすると、(7 - 余り) 日目 が答えになります。
# ただし、答えが7を超えた場合は 7日周期の中に収めるため、さらに調整します。

ans = 7 - rem

# もし計算結果が 8 になった場合などは、7日周期なので 1日目になります。
# 1 <= ans <= 7 の範囲に収めるための処理
if ans > 7:
    ans -= 7

print(ans)